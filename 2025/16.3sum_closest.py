class Solution:
    def threeSumClosest(self, nums: list[int], target: int) -> int:
        nums.sort()
        sum : int = nums[0] + nums[1] + nums[2]
        left : int = 1
        right : int = len(nums) - 1

        for i in range(len(nums) - 2):
            left = i + 1
            right = len(nums) - 1
            while left < right:
                currSum = nums[i] + nums[left] + nums[right]
                if abs(currSum -target) < abs(sum - target):
                    sum = currSum
                if currSum < target:
                    left += 1
                elif currSum > target:
                    right -= 1
                else: # direct hit
                    return target
        return sum

